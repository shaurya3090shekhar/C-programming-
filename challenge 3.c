//Write a program using "for loops" that prints 1000 times that "I'm sorry for not listening to my teacher I will practise more next time"

#include<stdio.h>
int main()
{
    int i;
    for(i=1; i<=1000; i++)
    {
        printf("I'm sorry for not listening to my teacher I will practise more next time \n");
    }
    return 0;
}